// Generate ...
func Generate() string {
	lenNames := len(names)
	lenAdj := len(adjectives)

	r := rand.New(rand.NewSource(time.Now().UnixNano()))
	in := r.Intn(lenNames)
	ia := r.Intn(lenAdj)

	return fmt.Sprintf("%s%s %s%s", string(adjectives[ia][0]+'Z'-'z'), adjectives[ia][1:], string(names[in][0]+'Z'-'z'), names[in][1:])
}
